  public checkProfanity(text: string): { isClean: boolean; match?: string } {
    if (typeof text !== 'string') return { isClean: true };

    let cleanText = this.removeFrequency(text);
    cleanText = this.removeDiacritics(cleanText);
    if (
      this.ARABIC_PROFANITY_LIST.includes(cleanText) ||
      this.ARABIC_PROFANITY_LIST.includes(text) ||
      this.ARABIC_POLITICAL_LIST.includes(cleanText) ||
      this.ARABIC_POLITICAL_LIST.includes(text) ||
      this.ARABIC_OFFENSIVE_HASHTAGS.includes(cleanText) ||
      this.ARABIC_OFFENSIVE_HASHTAGS.includes(text)
    )
      return {
        isClean: false,
        match: `"${text}"`,
      };
    for (const lang in this.filters) {
      const filter = this.filters[lang];
      if (filter.check(cleanText)) {
        return {
          isClean: false,
          match: `Language (${lang.toUpperCase()}): "${text}"`,
        };
      }
    }

    return { isClean: true };
  }

  private removeFrequency(text: string): string {
    return text
      .split(' ')
      .map((word) => Array.from(new Set(word.split(''))).join(''))
      .join(' ')
      .replace(/[.!@#$%^&*()~{}_0-9+\-\[\]]/g, '');
  }

  private removeDiacritics(text: string): string {
    return text.normalize('NFD').replace(/[\u0300-\u036f]/g, '');
  }
